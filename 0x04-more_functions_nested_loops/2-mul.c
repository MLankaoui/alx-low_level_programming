#include "main.h"

/**
 * mul - Returns the multiplication of two numbers.
 * @a: The first integer.
 * @b: The second integer.
 *
 * Description: This function multiplies two integers, `a` and `b`,
 * and returns the result.
 *
 * Return: The result of the multiplication.
 */
int mul(int a, int b)
{
	int R;

	R = a * b;
	return (R);
}
